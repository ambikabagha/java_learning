package arithmeticoperator;

public class ByteOperator {
	public static void main(String[] args) {
		byte a=102;
		byte b=90;
		int c=a+b;
		int d=a-b;
		int e=a*b;
		int f=a/b;
		int g=a%b;
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
	}

}
