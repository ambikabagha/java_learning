package arithmeticoperator;

public class DoubleOperator {
	public static void main(String[] args) {
		double a=12.34;
		double b=-1.90;
		double c=a+b;
		double d=a-b;
		double e=a*b;
		double f=a/b;
		double g=a%b;
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
	}
}
