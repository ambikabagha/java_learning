package arithmeticoperator;

public class FloatOperator {
	public static void main(String[] args) {
		float a=13.9f;
		float b=5.6f;
		float c=a+b;
		float d=a-b;
		float e=a*b;
		float f=a/b;
		float g=a%b;
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
	}

}
