package arithmeticoperator;

public class LongOperator {
	public static void main(String[] args) {
		long a=34565;
		long b=12334;
		long c=a+b;
		long d=a-b;
		long e=a*b;
		long f=a/b;
		long g=a%b;
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
	}

}
