package arithmeticoperator;

public class ShortOperator {
	public static void main(String[] args) {
		short a=32761;
		short b=32567;
		int c=a+b;
		int d=a-b;
		int e=a*b;
		int f=a/b;
		int g=a%b;
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
	}

}
