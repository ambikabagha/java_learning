package relationalOperator;

public class Example {
	
	public static void main(String[] args) {
		
		int a=5;
		int b=6;
		boolean c=a==b;
		
		System.out.println(a==b);
		
		System.out.println(a!=b);
		
		System.out.println(a<b);
		
		System.out.println(a<=b);
		
		System.out.println(a>b);
		
		System.out.println(a>=b);
					
		System.out.println(c);
		System.out.println(a==b);
		
	}

}
